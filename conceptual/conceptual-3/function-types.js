// types of function

// 1.no input, no output/no parameters, no return
// 2.no parameters, but return
// 3.parameters, no return
// 4.parameters, return

// 1.no input, no output/no parameters, no return
function fun1(){
    console.log('no parameter, no return')
}

// 2.no parameters, but return
function fun2(){
    return 5;
}

// 3.parameters, no return
function fun2(name){
    console.log(name)
}

// 4.parameters, return
function fun2(x,y){
    return x + y;
}